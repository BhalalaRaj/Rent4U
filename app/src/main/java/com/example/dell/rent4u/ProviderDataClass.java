package com.example.dell.rent4u;

class ProviderDataClass {

    String id, name, city, address;

    public ProviderDataClass(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCity() {
        return city;
    }

    public String getAddress() {
        return address;
    }
}
